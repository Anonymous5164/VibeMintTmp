// Function to upload metadata to IPFS (using Pinata)
async function uploadMetadataToIPFS(imageUrl: string, name: string, description: string): Promise<string> {
  const metadata = {
    name: name,
    description: description,
    image: imageUrl,
  };

  const pinataApiKey = process.env.PINATA_API_KEY || "";
  const pinataSecretApiKey = process.env.PINATA_SECRET_API_KEY || "";

  if (!pinataApiKey || !pinataSecretApiKey) {
    throw new Error(
      "Pinata API key and secret are required.  Set PINATA_API_KEY and PINATA_SECRET_API_KEY environment variables."
    );
  }

  const pinataSDK = require("@pinata/sdk");
  const pinata = new pinataSDK({
    pinataApiKey: pinataApiKey,
    pinataSecretApiKey: pinataSecretApiKey,
  });

  try {
    const options = {
      pinataOptions: {
        cidVersion: 0,
      },
    };
    const result = await pinata.pinJSONToIPFS(metadata, options);
    console.log("Metadata uploaded to IPFS:", result);
    return `ipfs://${result.IpfsHash}`; // Return IPFS URI
  } catch (error) {
    console.error("Failed to upload metadata to IPFS:", error);
    throw error; // Re-throw to stop the process
  }
}


async function mintNFT(recipientAddress: string, metadataURI: string) {
  try {
    const provider = new ethers.JsonRpcProvider(RPC_URL);
    const wallet = new ethers.Wallet(PRIVATE_KEY, provider);

    const contract = new ethers.Contract(
      CONTRACT_ADDRESS,
      CONTRACT_ABI,
      wallet
    );

    console.log(
      `Minting NFT to ${recipientAddress} with metadata URI: ${metadataURI}`
    );

    // **Important: The "transaction execution reverted" error often means the transaction is failing in the contract.**
    // Try explicitly setting the gas limit.  Start high and reduce.  This is often necessary for complex contracts.
    const gasLimit = 300000; // Adjust this value.  Start high, then refine.  Increased from 200000

    // **Check if the smart contract requires payment to mint.**
    // If it does, you need to send the correct amount of ETH along with the transaction.
    // The `value` field is used to specify the amount of ETH to send (in wei).
    const mintingPrice = ethers.parseEther("0.001"); // Example: Set the price to 0.001 ETH.  ***REPLACE THIS WITH THE ACTUAL PRICE IF NEEDED***
    const tx = await contract.mint(recipientAddress, metadataURI, {
      gasLimit: gasLimit,
     // value: mintingPrice, // Only include this if your contract requires payment. If not required, remove this line.
    });

    console.log(`Transaction hash: ${tx.hash}`);
    await tx.wait();
    console.log("NFT minted successfully!");
  } catch (error: any) { // Explicitly type 'error' as 'any'
    console.error("Minting error:", error);

    // **Improved Error Logging:  Log the specific error message from the revert, if available.**
    if (error.error && error.error.message) {
      console.error("Underlying revert message:", error.error.message);
    }
    if (error.message) {
        console.error("Error Message:", error.message);
    }
    if (error.code) {
        console.error("Error Code:", error.code);
    }
  }
}

